	def initialize_mode(self):
		"""
		Set interpreter mode flags based on self.args.mode.
		
		Sets CODE_MODE, SCRIPT_MODE, COMMAND_MODE, VISION_MODE, and CHAT_MODE according to the exact string value of self.args.mode ('code', 'script', 'command', 'vision', 'chat'). If none of the non-code modes are selected, ensures CODE_MODE is True by default.
		"""
		self.CODE_MODE = True if self.args.mode == 'code' else False
		self.SCRIPT_MODE = True if self.args.mode == 'script' else False
		self.COMMAND_MODE = True if self.args.mode == 'command' else False
		self.VISION_MODE = True if self.args.mode == 'vision' else False
		self.CHAT_MODE = True if self.args.mode == 'chat' else False
		if not self.SCRIPT_MODE and not self.COMMAND_MODE and not self.VISION_MODE and not self.CHAT_MODE:
			self.CODE_MODE = True
	
	def get_prompt(self, message: str, chat_history: List[dict]) -> List[dict] | str:
		"""
		Construct the prompt payload appropriate for the current interpreter mode and model.
		
		Builds a provider-specific messages payload (list of role/content dicts) used for LLM requests, or a plain system string for vision mode. The exact messages and strict instructions differ by mode:
		- code/script/command: system and assistant messages enforce returning exactly one fenced code block or single command/script with no extra explanation.
		- chat: includes a concise system instruction and, when provided, appends chat_history for context.
		- vision: returns a single system instruction string (not a messages list).
		For models whose identifier contains "claude", returns a single Anthropic-style user message with nested text chunks.
		
		Parameters:
			message (str): The user's current prompt or task.
			chat_history (List[dict]): Optional prior conversation entries to include for chat-mode context; expected as a list of role/content dictionaries.
		
		Returns:
			List[dict] | str: A messages list suitable for the target provider APIs, or a system instruction string when in vision mode.
		"""
		system_message: str = ""
		assistant_message = "Please generate code wrapped inside triple backticks known as codeblock."
		
		if self.CODE_MODE:
			system_message = (
				self.system_message
				+ "\nReturn exactly one executable code block."
				+ "\nDo not include explanations, comments, docstrings, markdown headings, or text outside the code block."
				+ "\nDo not add unrelated files, tables, charts, dataframes, package installs, subprocess calls, or network requests unless the user explicitly asks for them."
			)
			assistant_message = (
				f"Return only executable {self.INTERPRETER_LANGUAGE} code wrapped in triple backticks."
				f" No explanations. No comments. No text outside the code block."
				f" Do not add extra libraries or side effects unless required by the task."
			)
		elif self.SCRIPT_MODE:
			system_message = (
				"Please generate a well-written script that is precise, easy to understand, and compatible with the current operating system."
				"\nReturn exactly one executable code block with no explanations or extra text."
			)
			assistant_message = "Return only the script wrapped in triple backticks. No explanations or comments."
		elif self.COMMAND_MODE:
			system_message = (
				"Please generate a single line command that is precise, easy to understand, and compatible with the current operating system."
				"\nReturn only the command with no explanations or extra text."
			)
			assistant_message = "Return only the command wrapped in triple backticks. No explanations."
		elif self.VISION_MODE:
			system_message = "Please generate a well-written description of the image that is precise, easy to understand"
			return system_message
		elif self.CHAT_MODE:
			system_message = "Please generate a well-written response that is precise, easy to understand"
			
			# Add the chat history to the prompt
			if chat_history or len(chat_history) > 0:
				system_message += "\n\n" + "\n\n" + "This is user chat history for this task and make sure to use this as reference to generate the answer if user asks for 'History' or 'Chat History'.\n\n" + "\n\n" + str(chat_history) + "\n\n"
		
		# Use the Messages API from Anthropic.
		if 'claude' in self.INTERPRETER_MODEL:
			messages = [
					{
						"role": "user",
						"content": [
							{
								"type": "text",
								"text": message
							}
						]
					}
				]

		# Use the Assistants API.
		else:
			messages = [
				{"role": "system", "content": system_message},
				{"role": "assistant", "content": assistant_message},
				{"role": "user", "content": message}
			]
		
		return messages
	
	def execute_last_code(self, os_name):
		"""
		Display the most recently saved or generated code and run it for the specified operating system.
		
		Retrieves the last saved output for the current interpreter mode/language, shows a message if no saved code is available, renders the code to the user, and then attempts execution through the interpreter's guarded execution path. Execution results and errors are displayed to the user and recorded in the logger.
		Parameters:
			os_name (str): Target operating system identifier used to locate the saved output and to select the platform-specific execution path.
		"""
		try:
			code_file, code_snippet = self.utility_manager.get_output_history(mode=self.INTERPRETER_MODE, os_name=os_name, language=self.INTERPRETER_LANGUAGE)

			# check if the code is empty
			if code_snippet is None or code_file is None:
				self.logger.error("Code history or file is empty.")
				display_markdown_message("Code history or file is empty. - Please use **-s** flag or **/save** command to save the code.")
				return
			
			display_code(code_snippet)  # Display the code first.

			# Execute the code if the user has selected.
			code_output, code_error = self._execute_generated_output(code_snippet, os_name)
			if code_output:
				self.logger.info(f"{self.INTERPRETER_LANGUAGE} code executed successfully.")
				display_code(code_output)
				self.logger.info(f"Output: {code_output[:100]}")
			elif code_error:
				self.logger.info(f"{self.INTERPRETER_LANGUAGE} code executed with error.")
				display_markdown_message(f"Error: {code_error}")
		except Exception as exception:
			self.logger.error(f"Error in processing command run code: {str(exception)}")
			raise

	def _extract_latest_user_text(self, message, messages):
		"""
		Extract the most recent user-provided text from a direct message or a conversation messages list.
		
		Search order and behavior:
		- If `message` is a non-empty string, return it trimmed.
		- Otherwise, if `messages` is a list, scan it in reverse for the last item with `"role" == "user"`.
		  - If that user's `"content"` is a non-empty string, return it trimmed.
		  - If the user's `"content"` is a list of chunk dicts, extract chunks where `chunk["type"] == "text"`, join their `text` fields with spaces, and return the result if non-empty.
		- If no user text is found, return the literal string "Help with this request."
		
		Parameters:
			message: a direct message string or other value to consider first.
			messages: an optional list of message dicts (each may include "role" and "content") to search when `message` is empty.
		
		Returns:
			Extracted user text (trimmed) when available, otherwise the fallback string "Help with this request."
		"""
		if isinstance(message, str) and message.strip():
			return message.strip()
		if isinstance(messages, list):
			for item in reversed(messages):
				if not isinstance(item, dict) or item.get("role") != "user":
					continue
				content = item.get("content")
				if isinstance(content, str) and content.strip():
					return content.strip()
				if isinstance(content, list):
					text_parts = []
					for chunk in content:
						if isinstance(chunk, dict) and chunk.get("type") == "text":
							text_parts.append(str(chunk.get("text", "")).strip())
					joined = " ".join(part for part in text_parts if part)
					if joined:
						return joined
		return "Help with this request."

	def _run_openai_compatible_completion(self, api_key_name, messages, temperature, max_tokens, api_base, extra_headers=None):
		"""
		Send a completion request to an OpenAI-compatible LLM provider using credentials from the environment.
		
		Parameters:
			api_key_name (str): Name of the environment variable that holds the API key.
			messages (list|dict): Message payload formatted for the model provider.
			temperature (float): Sampling temperature for the completion.
			max_tokens (int): Maximum tokens to generate.
			api_base (str): Base URL for the model API; must be set for custom models.
			extra_headers (dict, optional): Additional HTTP headers to include with the request.
		
		Returns:
			The response object returned by litellm.completion for the configured model.
		
		Raises:
			Exception: If the specified API key environment variable is missing.
			Exception: If `api_base` equals the string 'None' (i.e., not configured).
		"""
		api_key = os.getenv(api_key_name)
		if not api_key:
			raise Exception(f"{api_key_name} not found in .env file.")
		if api_base == 'None':
			raise Exception("Exception api base not set for custom model")
		custom_llm_provider = "openai"
		completion_kwargs = {
			"messages": messages,
			"temperature": temperature,
			"max_tokens": max_tokens,
			"api_base": api_base,
			"api_key": api_key,
			"custom_llm_provider": custom_llm_provider,
		}
		if extra_headers:
			completion_kwargs["extra_headers"] = extra_headers
		return litellm.completion(self.INTERPRETER_MODEL, **completion_kwargs)

	def _generate_browser_use_content(self, message, messages, config_values):
		"""
		Use the Browser Use API to run a browsing task and return the session result.
		
		Parameters:
			message (str): The latest user message fallback used when extracting the task.
			messages (list|None): Conversation message history used to extract the most recent user text.
			config_values (Mapping): Configuration overrides; supports keys:
				- "api_base": base URL for the Browser Use API (default "https://api.browser-use.com/api/v3")
				- "browser_use_timeout": maximum seconds to wait for completion (default 150)
				- "browser_use_poll_interval": seconds between poll attempts (default 3)
		
		Returns:
			str: The session output as a string; if the output is a dict or list it is returned as a JSON string.
		
		Raises:
			Exception: If BROWSER_USE_API_KEY is missing, session creation fails or returns no id, the session reports a terminal failure status, or the session times out.
		"""
		api_key = os.getenv("BROWSER_USE_API_KEY")
		if not api_key:
			raise Exception("BROWSER_USE_API_KEY not found in .env file.")

		base_url = str(config_values.get("api_base", "https://api.browser-use.com/api/v3")).rstrip("/")
		model = self.INTERPRETER_MODEL
		task = self._extract_latest_user_text(message, messages)
		timeout_seconds = int(config_values.get("browser_use_timeout", 150))
		poll_interval = int(config_values.get("browser_use_poll_interval", 3))

		headers = {
			"X-Browser-Use-API-Key": api_key,
			"Content-Type": "application/json",
		}
		payload = {
			"task": task,
			"model": model,
			"keepAlive": False,
		}
		self.logger.info(f"Starting Browser Use session with model={model}")
		create_response = requests.post(f"{base_url}/sessions", headers=headers, json=payload, timeout=45)
		create_response.raise_for_status()
		create_data = create_response.json()
		session_id = create_data.get("id")
		if not session_id:
			raise Exception("Browser Use session creation failed: missing session id")

		end_time = time.time() + timeout_seconds
		while time.time() < end_time:
			status_response = requests.get(f"{base_url}/sessions/{session_id}", headers=headers, timeout=30)
			status_response.raise_for_status()
			status_data = status_response.json()
			status = str(status_data.get("status", "")).lower()
			live_url = status_data.get("liveUrl")

			if status in {"finished", "completed", "stopped", "done", "success"}:
				output = status_data.get("output")
				if output is None:
					output = status_data.get("result")
				if output is None:
					output = status_data.get("finalResult")
				if output is None and live_url:
					output = f"Browser Use session completed. Live URL: {live_url}"
				if output is None:
					output = f"Browser Use session completed. Session ID: {session_id}"
				if isinstance(output, (dict, list)):
					return json.dumps(output, ensure_ascii=False)
				return str(output)

			if status in {"failed", "error", "cancelled"}:
				raise Exception(f"Browser Use session failed with status '{status}'")

			time.sleep(poll_interval)

		raise Exception("Browser Use session timed out.")
	
	def generate_content(self, message, chat_history: list[tuple[str, str]], temperature=0.1, max_tokens=1024,config_values=None,image_file=None):
		"""
		Generate model output for the current interpreter mode and return the extracted text content.
		
		This builds provider-specific request payloads from the given message and chat history, calls the appropriate LLM or vision endpoint (handling provider/model-specific mappings and API base overrides), and returns the utility-extracted textual result.
		
		Parameters:
			message (str): The user's prompt or task to send to the model.
			chat_history (list[tuple[str, str]]): Conversation history as (role, text) pairs to include in the request.
			temperature (float): Sampling temperature for the model (lower values make outputs more deterministic).
			max_tokens (int): Maximum number of tokens to request from the model.
			config_values (dict | None): Optional config overrides (e.g., 'temperature', 'max_tokens', 'api_base', 'provider') that influence provider selection and request parameters.
			image_file (str | None): Path or URL to an image when using vision models; required for vision-mode Gemini calls.
		
		Returns:
			generated_text (str): The text extracted from the model/vision response suitable for downstream processing or display.
		"""
		self.logger.info(f"Generating content with args: message={message}, chat_history={chat_history}, temperature={temperature}, max_tokens={max_tokens}, config_values={config_values}, image_file={image_file}")
		self.logger.info(f"Interpreter model selected is '{self.INTERPRETER_MODEL}'")
		api_base = 'None'
		config_provider = ""
		
		# Use the values from the config file if they are provided
		if config_values:
			temperature = float(config_values.get('temperature', temperature))
			max_tokens = int(config_values.get('max_tokens', max_tokens))
			api_base = str(config_values.get('api_base', None)) # Only for OpenAI.
			config_provider = str(config_values.get("provider", "")).strip().lower()

		# Get the system prompt
		messages = self.get_prompt(message, chat_history)
		
		# Provider-specific OpenAI-compatible endpoints.
		if config_provider == "nvidia" or self.INTERPRETER_MODEL.startswith("nvidia/"):
			self.logger.info("Model is NVIDIA via OpenAI-compatible API.")
			response = self._run_openai_compatible_completion("NVIDIA_API_KEY", messages, temperature, max_tokens, api_base)
			self.logger.info("Response received from completion function.")

		elif config_provider in ("z-ai", "zai") or self.INTERPRETER_MODEL.startswith(("glm-", "z-ai/", "zai/")):
			self.logger.info("Model is Z AI via OpenAI-compatible API.")
			response = self._run_openai_compatible_completion("Z_AI_API_KEY", messages, temperature, max_tokens, api_base)
			self.logger.info("Response received from completion function.")

		elif config_provider in ("browser-use", "browser_use") or self.INTERPRETER_MODEL.startswith(("bu-", "browser-use/")):
			self.logger.info("Model is Browser Use session model.")
			response_text = self._generate_browser_use_content(message, messages, config_values or {})
			self.logger.info("Response received from Browser Use session.")
			return response_text

		elif config_provider == "openrouter":
			self.logger.info("Model is OpenRouter via OpenAI-compatible API.")
			extra_headers = {
				"HTTP-Referer": "https://github.com/haseeb-heaven/code-interpreter",
				"X-OpenRouter-Title": "Code Interpreter",
			}
			response = self._run_openai_compatible_completion("OPENROUTER_API_KEY", messages, temperature, max_tokens, api_base, extra_headers=extra_headers)
			self.logger.info("Response received from OpenRouter completion.")

		# Check if the model is from OpenAI (GPT/o-series)
		elif self.INTERPRETER_MODEL.startswith(("gpt", "o1", "o3", "o4")):
			self.logger.info("Model is OpenAI GPT/o-series")
			reasoning_model = self.INTERPRETER_MODEL.startswith(("o1", "o3", "o4", "gpt-5"))
			completion_kwargs = {
				"messages": messages,
				"max_tokens": max_tokens,
			}
			if not reasoning_model:
				completion_kwargs["temperature"] = temperature
			else:
				# O-series and GPT-5 models reject some classic params (e.g., temperature != 1)
				completion_kwargs["drop_params"] = True

			if api_base != 'None':
				# Set the custom language model provider
				completion_kwargs["custom_llm_provider"] = "openai"
				completion_kwargs["api_base"] = api_base
				self.logger.info(f"Custom API mode selected for OpenAI, api_base={api_base}")
			else:
				if self.INTERPRETER_MODEL.startswith(("o1", "o3", "o4")):
					completion_kwargs["custom_llm_provider"] = "openai"
				self.logger.info("Default API mode selected for OpenAI.")
			response = litellm.completion(self.INTERPRETER_MODEL, **completion_kwargs)
			self.logger.info("Response received from completion function.")
		
		# Check if the model is Gemini Pro
		elif 'gemini' in self.INTERPRETER_MODEL:

			if self.INTERPRETER_MODE == 'vision':
				# Import Gemini Vision only if the model is Gemini Pro Vision.
				try:
					from libs.gemini_vision import GeminiVision
					self.gemini_vision = GeminiVision()
				except Exception as exception:
					self.logger.error(f"Error importing Gemini Vision: {exception}")
					raise

				self.logger.info("Model is Gemini Pro Vision.")
				response = None

				# Check if image_file is valid.
				if not image_file:
					self.logger.error("Image file is not valid or Corrupted.")
					raise ValueError("Image file is not valid or Corrupted.")
				
				# Check if image contains URL.
				if 'http' in image_file or 'https' in image_file or 'www.' in image_file:
					self.logger.info("Image contains URL.")
					response = self.gemini_vision.gemini_vision_url(prompt=messages,image_url=image_file)
				else:
					self.logger.info("Image contains file.")
					response = self.gemini_vision.gemini_vision_path(prompt=messages,image_path=image_file)
				
				self.logger.info("Response received from completion function.")
				return response # Return the response from Gemini Vision because its not coding model.
			else:
				self.logger.info("Model is Gemini.")
